speed = int(input('Введите скорость в километрах в час: '))
time =  int(input('Введите время поездки в часах: '))

print('------------------------------------')
print(f'{'Час':^10}|{'Пройденное расстояние':^25}')
print('------------------------------------')

for i in range(time):
    print(f'{i + 1:^10}|{(i + 1) * speed:^25}')
    print('------------------------------------')